     public void chk(char op) throws IOException {
        FileInputStream in = new FileInputStream("/home/killer/Desktop/myf/ans.txt");
        char s = 'n';
        for (int i = 0; i < qno; i++) {
            int j = in.read();
            s = (char) j;

        }
        if (s == op) {
            score += 10;
            scr.setText("Score : " + score);
            msg.setText("Correct ans" + s);
            skp.doClick(1);
        } else {
            msg.setText("ooopsss wrong.." + s);
            skp.doClick(1);
        }

    }
